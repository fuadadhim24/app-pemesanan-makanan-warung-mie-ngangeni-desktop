    private void btnsumbitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsumbitMouseClicked
        String finaljob = job.getText();
        String usernameText = username.getText();
        usernameText = usernameText.replaceAll("!", "");
        System.out.println(finaljob);
        if (password.getText().equals("***************")) {
            password.setText(null);
        }
            try {
                String sql = "UPDATE `akun` SET `password` = '"+password.getText()+"' WHERE akun.username = ?";
                java.sql.Connection conn = (java.sql.Connection) Config.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
                pst.setString(1, usernameText);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Password Berhasil Diperbarui");
                if (finaljob.equals("ADMIN")) {
                        JOptionPane.showMessageDialog(null, "Berhasil login");
                        new AdminPenjualan().setVisible(true);
                        this.dispose();
                } else if (finaljob.equals("KOKI")) {
                        JOptionPane.showMessageDialog(null, "Berhasil login");
                        new koki().setVisible(true);
                        this.dispose();
                } else if (finaljob.equals("KASIR")) {
                        JOptionPane.showMessageDialog(null, "Berhasil login");
                        new kasir().setVisible(true);
                        this.dispose();
                } else if (finaljob.equals("WAITER")) {
                        JOptionPane.showMessageDialog(null, "Berhasil login");
                        new koki().setVisible(true);
                        this.dispose();
                    }
                conn.close();
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        
        
    }//GEN-LAST:event_btnsumbitMouseClicked

